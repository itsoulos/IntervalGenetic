# include <cprogram.h>
# include <stdio.h>
# include <math.h>
# include <QDebug>



Cprogram::Cprogram(int dim,int pdim)
{
	dimension = dim;
	pdimension = pdim;
	makeTerminals();
	makeNonTerminals();
	makeRules();
}

int	Cprogram::newRule()
{
	int r;
	r=rule.size();
	rule.resize(r+1);
	rule[r]=new Rule();
	return r;
}

void	Cprogram::makeTerminals()
{
	Number.set("Number",1);
	In.set("in",1);
	Delim.set("#",1);
	PI.set("pi",1);
	Plus.set("+",1);
	Minus.set("-",1);
	Mult.set("*",1);
	Div.set("/",1);
	Pow.set("^",1);
	Comma.set(",",1);
	Dot.set(".",1);
	Lpar.set("(",1);
	Rpar.set(")",1);

	Sin.set("sin",1);
	Cos.set("cos",1);
	Exp.set("exp",1);
	Log.set("log",1);
	Not.set("!",1);
	addN.set("Add",1);
	subN.set("Sub",1);
	multN.set("Mult",1);
	divN.set("Div",1);
	
	Abs.set("abs",1);
	Sqrt.set("sqrt",1);
	Tan.set("tan",1);
	Int.set("int",1);
	Log10.set("log10",1);
	Gt.set(">",1);
	Ge.set(">=",1);
	Lt.set("<",1);
	Le.set("<=",1);
	Eq.set("=",1);
	Neq.set("!=",1);
	And.set("&",1);
	Or.set("|",1);
	boolExpr.set("jump",1);
	XX.resize(dimension);
	Avg.set("sig",1);
	vars="";
	for(int i=0;i<dimension;i++)
	{
		char str[100];
		sprintf(str,"x%d",i+1);
		XX[i].set(str,1);
		vars=vars+str;
		if(i<dimension-1) vars=vars+",";
	}

	Digit.resize(10);
	for(int i=0;i<10;i++)
	{
		char str[100];
		sprintf(str,"%d",i);
		Digit[i].set(str,1);
	}
}

void	Cprogram::makeNonTerminals()
{
	Start.set("START",0);
	xlist.set("XLIST",0);
	DigitList.set("DIGITLIST",0);
	Digit0.set("DIGIT0",0);
	Digit1.set("DIGIT1",0);
	XXlist.set("XXLIST",0);
	Expr.set("EXPR",0);
	function.set("FUNCTION",0);
	binaryop.set("BINARYOP",0);
	terminal.set("TERMINAL",0);
	MinMax.set("MINMAX",0);
	BooleanExpr.set("BOOLEXPR",0);
	boolop.set("BOOLOP",0);
	inexpr.set("INEXPR",0);
	infunction.set("INFUNCTION",0);
	andor.set("ANDOR",0);
	nearfunction.set("NEARFUNCTION",0);
}

void	Cprogram::makeRules()
{
	int r;
        r=newRule();
        rule[r]->addSymbol(&BooleanExpr);
        Start.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&XXlist);
	rule[r]->addSymbol(&boolop);
	rule[r]->addSymbol(&Expr);
	BooleanExpr.addRule(rule[r]);
	
	r=newRule();
	rule[r]->addSymbol(&Not);
	rule[r]->addSymbol(&Lpar);
	rule[r]->addSymbol(&BooleanExpr);
	rule[r]->addSymbol(&Rpar);
	BooleanExpr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&XXlist);
	rule[r]->addSymbol(&boolop);
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&And);
	rule[r]->addSymbol(&BooleanExpr);
	BooleanExpr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&XXlist);
	rule[r]->addSymbol(&boolop);
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&Or);
	rule[r]->addSymbol(&BooleanExpr);
	BooleanExpr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Gt);
	boolop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Ge);
	boolop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Lt);
	boolop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Le);
	boolop.addRule(rule[r]);
	
	r=newRule();
	rule[r]->addSymbol(&Lpar);
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&binaryop);
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&Rpar);
	Expr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&function);
        rule[r]->addSymbol(&Lpar);
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&Rpar);
	Expr.addRule(rule[r]);
		
	r=newRule();
	rule[r]->addSymbol(&Log);
	function.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Exp);
	function.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Sin);
	function.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Cos);
	function.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Plus);
	binaryop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Minus);
	binaryop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Mult);
	binaryop.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Div);
	binaryop.addRule(rule[r]);
	
	r=newRule();
	rule[r]->addSymbol(&Digit0);
	DigitList.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Digit0);
	rule[r]->addSymbol(&Digit0);
	//rule[r]->addSymbol(&DigitList);
	DigitList.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Digit0);
	rule[r]->addSymbol(&Digit0);
	rule[r]->addSymbol(&Digit0);
	DigitList.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&DigitList);
	rule[r]->addSymbol(&Dot);
	rule[r]->addSymbol(&DigitList);
	terminal.addRule(rule[r]);
    Expr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&Lpar);	
	rule[r]->addSymbol(&Minus);
	rule[r]->addSymbol(&DigitList);
	rule[r]->addSymbol(&Dot);
	rule[r]->addSymbol(&DigitList);
	rule[r]->addSymbol(&Rpar);
    terminal.addRule(rule[r]);
    Expr.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&XXlist);
	Expr.addRule(rule[r]);

	for(int i=0;i<dimension;i++)
	{
		r=newRule();
		rule[r]->addSymbol(&XX[i]);
		XXlist.addRule(rule[r]);
	}

	r=newRule();
	rule[r]->addSymbol(&Expr);
	rule[r]->addSymbol(&Mult);
	rule[r]->addSymbol(&XXlist);
	XXlist.addRule(rule[r]);

	r=newRule();
	rule[r]->addSymbol(&XXlist);
	rule[r]->addSymbol(&binaryop);
	rule[r]->addSymbol(&XXlist);
	XXlist.addRule(rule[r]);

	for(int i=0;i<10;i++)
	{
		r=newRule();
		rule[r]->addSymbol(&Digit[i]);
		Digit0.addRule(rule[r]);
	}

}

int		Cprogram::Parse(string expr)
{
    int retcode=parser.Parse(expr,vars);
    if(retcode!=-1)
    {
        qDebug()<<"RetCode is "<<retcode<<" String is "<<
                  QString(expr.c_str())<<" Vars are "<<QString(vars.c_str())<<endl;
    }
    return retcode==-1;//(parser.Parse(expr,vars)==-1);
}

int		Cprogram::EvalError()
{
	return	parser.EvalError();
}

double	Cprogram::Eval( const double *X)
{
		return parser.Eval(X);
}

Symbol	*Cprogram::getStartSymbol()
{
	return &Start;
}

Cprogram::~Cprogram()
{
	for(int i=0;i<rule.size();i++)
		delete rule[i];
}
